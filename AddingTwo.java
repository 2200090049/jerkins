public class AddTwoNumbers {
    public static void main(String[] args) {
        // Hardcoded numbers
        double num1 = 5.2;
        double num2 = 3.8;

        // Add the two numbers
        double sum = num1 + num2;

        // Display the result
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}
